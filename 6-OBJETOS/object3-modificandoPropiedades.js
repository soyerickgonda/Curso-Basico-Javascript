//MODIFICAR EL VALOR DE UNA PROPIEDAD DE UN OBJETO
const persona = {
    nombre: "Pedro",
    apellido: "Perez",
    edad: 30
}
persona.nombre = "Gilfoy";
console.log(persona.nombre); // Gilfoy
console.log(persona.apellido); //Perez
console.log(persona.edad); // 30

